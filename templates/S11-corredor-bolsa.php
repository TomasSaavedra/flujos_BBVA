	var tamanio = 0;

	$('#nextAction').on('click, vclick', function() {
		
		if($('#radio4').is(':checked') && $('#radio8').is(':checked') && $('#radio12').is(':checked')){
			$('#modalCarta').click();
		}else{
			if($('.enrolamiento-menu').first().is(':visible')){
				$('#modalFirst').click();
			}
			$( ".progress-display" ).animate({ "width": tamanioProgress() + "%" }, "slow" );
			$('.point-background.active').next().addClass('active');

			$('.enrolamiento-menu.active').hide();
			$('.enrolamiento-menu.active').next().show("slide", { direction: "right" }, 500);

			setTimeout(function () {
				$('.enrolamiento-menu.active').next().addClass('active');
				$('.enrolamiento-menu.active').prev().removeClass('active')
			}, 550);
			$('#prevAction').show();

			if($('.enrolamiento-menu').last().is(':visible')){
				$('.boxBottom').hide();
			}
		}
	});

	function tamanioProgress(){
		tamanio = tamanio + 33;

		return tamanio;
	}

	function tamanioDesProgress(){
		tamanio = tamanio - 33;

		return tamanio;
	}

	$('#prevAction').on('click, vclick', function() {

		$( ".progress-display" ).animate({ "width": tamanioDesProgress() + "%" }, "slow" );
		$('.point-background.active').last().removeClass('active');

		$('.enrolamiento-menu.active').hide();
		$('.enrolamiento-menu.active').prev().show("slide", { direction: "left" }, 500);

		setTimeout(function () {
			$('.enrolamiento-menu.active').prev().addClass('active');
			$('.enrolamiento-menu.active').next().removeClass('active')
		}, 550);

		if($('.enrolamiento-menu:first').is(':visible')){
			$('#prevAction').hide();
		}

		if($('.enrolamiento-menu').last().is(':visible')){
			$('.boxBottom').hide();
		}
	});

	$(document).ready(function(){
		$.each($('.enrolamiento-menu'),function(i,menu){
			if($(this).is(':visible')){
				$(this).addClass('active');
				$('#prevAction').hide();
			}
		});
	});

